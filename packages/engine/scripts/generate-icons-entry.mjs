import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import * as lucideIcons from 'lucide-react'

const excludedLucideExports = new Set(['createLucideIcon', 'Icon', 'icons', 'LucideProvider', 'useLucideContext'])

const isLucideIconExport = (value) => {
  if (typeof value !== 'object' || value === null) {
    return false
  }

  return '$$typeof' in value && 'render' in value
}

const iconNames = Object.entries(lucideIcons)
  .filter(([name, value]) => !excludedLucideExports.has(name) && isLucideIconExport(value))
  .map(([name]) => name)
  .sort()

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const outputFilePath = path.join(__dirname, '..', 'src', 'generated', 'icons.ts')
const iconNamesOutputFilePath = path.join(__dirname, '..', 'src', 'generated', 'iconNames.ts')
const iconNodesOutputFilePath = path.join(__dirname, '..', 'src', 'generated', 'iconNodes.ts')
const iconImportPathsOutputFilePath = path.join(__dirname, '..', 'src', 'generated', 'iconImportPaths.ts')

const renderIconNode = (iconComponent, iconName) => {
  const iconElement = iconComponent.render({}, null)
  const iconNode = iconElement.props?.iconNode

  if (!iconNode) {
    throw new Error(`Unable to read lucide-react icon node for docs icon "${iconName}".`)
  }

  return iconNode
}

const iconEntries = Object.entries(lucideIcons)
  .filter(([name, value]) => !excludedLucideExports.has(name) && isLucideIconExport(value))
  .sort(([left], [right]) => left.localeCompare(right))

const iconNodeEntries = []
const iconNodeReferences = []
const iconNodeReferenceByComponent = new Map()
const lucideReactEntryPath = fileURLToPath(import.meta.resolve('lucide-react/dist/esm/lucide-react.mjs'))
const lucideReactEntrySource = fs.readFileSync(lucideReactEntryPath, 'utf8')
const lucideIconImportPathByName = new Map()

for (const [, exportSpecifiers, iconFileName] of lucideReactEntrySource.matchAll(
  /export \{([^}]+)\} from '\.\/icons\/([^']+\.mjs)';/g,
)) {
  for (const [, iconName] of exportSpecifiers.matchAll(/default as ([A-Za-z_$][\w$]*)/g)) {
    lucideIconImportPathByName.set(iconName, `lucide-react/dist/esm/icons/${iconFileName}`)
  }
}

for (const [iconName, iconComponent] of iconEntries) {
  const currentReference = iconNodeReferenceByComponent.get(iconComponent)

  if (currentReference) {
    iconNodeReferences.push({ iconName, reference: currentReference })
    continue
  }

  const reference = `iconNode${iconNodeEntries.length}`
  iconNodeReferenceByComponent.set(iconComponent, reference)
  iconNodeEntries.push({ reference, iconName, iconNode: renderIconNode(iconComponent, iconName) })
  iconNodeReferences.push({ iconName, reference })
}

const outputSource = [
  '// This file is generated by packages/engine/scripts/generate-icons-entry.mjs.',
  '// Do not edit it by hand.',
  '',
  "export type { LucideIcon } from 'lucide-react'",
  ...iconNames.map((iconName) => `export { ${iconName} } from 'lucide-react'`),
  '',
].join('\n')

const iconNamesOutputSource = [
  '// This file is generated by packages/engine/scripts/generate-icons-entry.mjs.',
  '// Do not edit it by hand.',
  '',
  'export const docsGeneratedIconNames = [',
  ...iconNames.map((iconName) => `  ${JSON.stringify(iconName)},`),
  '] as const',
  '',
  'export type DocsGeneratedIconName = (typeof docsGeneratedIconNames)[number]',
  '',
].join('\n')

const iconNodesOutputSource = [
  '// This file is generated by packages/engine/scripts/generate-icons-entry.mjs.',
  '// Do not edit it by hand.',
  '',
  "import type { DocsGeneratedIconName } from './iconNames.js'",
  '',
  'type DocsGeneratedIconNode = [tagName: string, attrs: Record<string, string>][]',
  '',
  ...iconNodeEntries.flatMap(({ reference, iconNode }) => [
    `const ${reference}: DocsGeneratedIconNode = ${JSON.stringify(iconNode)}`,
    '',
  ]),
  'const docsGeneratedIconNodes = {',
  ...iconNodeReferences.map(({ iconName, reference }) => `  ${JSON.stringify(iconName)}: ${reference},`),
  '} satisfies Record<DocsGeneratedIconName, DocsGeneratedIconNode>',
  '',
  'export const getGeneratedDocsIconNode = (iconName: DocsGeneratedIconName) => {',
  '  return docsGeneratedIconNodes[iconName]',
  '}',
  '',
].join('\n')

const iconImportPathsOutputSource = [
  '// This file is generated by packages/engine/scripts/generate-icons-entry.mjs.',
  '// Do not edit it by hand.',
  '',
  "import type { DocsGeneratedIconName } from './iconNames.js'",
  '',
  'export const docsGeneratedIconImportPaths = {',
  ...iconNames.map((iconName) => {
    const iconImportPath = lucideIconImportPathByName.get(iconName)

    if (!iconImportPath) {
      throw new Error(`Unable to resolve lucide-react import path for docs icon "${iconName}".`)
    }

    return `  ${JSON.stringify(iconName)}: ${JSON.stringify(iconImportPath)},`
  }),
  '} as const satisfies Record<DocsGeneratedIconName, string>',
  '',
].join('\n')

const writeFileIfChanged = (filePath, source) => {
  const currentSource = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : null

  if (currentSource !== source) {
    fs.writeFileSync(filePath, source)
  }
}

fs.mkdirSync(path.dirname(outputFilePath), { recursive: true })

writeFileIfChanged(outputFilePath, outputSource)
writeFileIfChanged(iconNamesOutputFilePath, iconNamesOutputSource)
writeFileIfChanged(iconNodesOutputFilePath, iconNodesOutputSource)
writeFileIfChanged(iconImportPathsOutputFilePath, iconImportPathsOutputSource)
